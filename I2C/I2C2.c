//------------------------------------------------------------------------------
// Includes

#include "definitions.h"
#include "I2C2.h"
#include <stddef.h>
#include "Timer/Timer.h"

//------------------------------------------------------------------------------
// Function declarations

static void WaitForInterruptOrTimeout(void);

//------------------------------------------------------------------------------
// Variables

static I2CMessage* currentMessage;
static uint64_t messageTimeout;

//------------------------------------------------------------------------------
// Functions

/**
 * @brief Initialises the module.
 * @param clockFrequency Clock frequency.
 */
void I2C2Initialise(const I2CClockFrequency clockFrequency) {

    // Ensure default register states
    I2C2Deinitialise();

    // Configure I2C
    I2C2BRG = I2CCalculateI2Cxbrg(clockFrequency);
    if (clockFrequency != I2CClockFrequency400kHz) {
        I2C2CONbits.DISSLW = 1; // Slew rate control disabled
    }
    I2C2CONbits.I2CEN = 1; // Enables the I2C module and configures the SDA and SCL pins as serial port pins
}

/**
 * @brief Disable the module.
 */
void I2C2Deinitialise(void) {

    // Disable I2C and restore default register states
    I2C2CON = 0;
    I2C2STAT = 0;

    // Disable interrupt
    EVIC_SourceDisable(INT_SOURCE_I2C2_MASTER);
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
}

/**
 * @brief Generates a start event.
 */
void I2C2Start(void) {
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2CONbits.SEN = 1;
    WaitForInterruptOrTimeout();
}

/**
 * @brief Generates a repeated start event.
 */
void I2C2RepeatedStart(void) {
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2CONbits.RSEN = 1;
    WaitForInterruptOrTimeout();
}

/**
 * @brief Generates a stop event.
 */
void I2C2Stop(void) {
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2CONbits.PEN = 1;
    WaitForInterruptOrTimeout();
}

/**
 * @brief Sends a byte and checks for ACK.
 * @param byte Byte.
 * @return True if an ACK was generated.
 */
bool I2C2Send(const uint8_t byte) {
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2STATbits.ACKSTAT = 0;
    I2C2TRN = byte;
    WaitForInterruptOrTimeout();
    return I2C2STATbits.ACKSTAT == 0;
}

/**
 * @brief Receives a byte and generates an ACK or NACK.
 * @param ack True for ACK.
 * @return Byte.
 */
uint8_t I2C2Receive(const bool ack) {

    // Receive
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2CONbits.RCEN = 1;
    WaitForInterruptOrTimeout();

    // ACK/NACK
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER);
    I2C2CONbits.ACKDT = ack ? 0 : 1;
    I2C2CONbits.ACKEN = 1;
    WaitForInterruptOrTimeout();
    return I2C2RCV;
}

/**
 * @brief Waits for the interrupt or timeout.
 */
static void WaitForInterruptOrTimeout(void) {
    const uint64_t timeout = TimerGetTicks64() + (TIMER_TICKS_PER_SECOND / (I2CClockFrequency100kHz / 10)); // 10 clock cycles timeout for slowest clock
    while (true) {
        if (EVIC_SourceStatusGet(INT_SOURCE_I2C2_MASTER)) {
            break;
        }
        if (TimerGetTicks64() > timeout) {
            break;
        }
    }
}

/**
 * @brief Begins message.
 * @param message Message.
 */
void I2C2BeginMessage(I2CMessage * const message) {

    // Do nothing if message in progress
    if (I2C2MessageInProgress()) {
        return;
    }

    // Set current message
    currentMessage = message;
    message->index = 0;

    // Calculate message timeout
    messageTimeout = TimerGetTicks64() + (I2C_MESSAGE_MAX_LENGTH * I2C_TIMEOUT);

    // Trigger first interrupt
    EVIC_SourceStatusSet(INT_SOURCE_I2C2_MASTER);
    EVIC_SourceEnable(INT_SOURCE_I2C2_MASTER);
}

/**
 * @brief Returns true if the message is in progress.
 * @return True if the message is in progress.
 */
bool I2C2MessageInProgress(void) {
    if (TimerGetTicks64() >= messageTimeout) {
        EVIC_SourceDisable(INT_SOURCE_I2C2_MASTER);
    }
    return EVIC_SourceIsEnabled(INT_SOURCE_I2C2_MASTER);
}

/**
 * @brief I2C interrupt handler.  This function should be called by the ISR
 * implementation generated by MPLAB Harmony.
 */
void I2C2InterruptHandler(void) {
    EVIC_SourceStatusClear(INT_SOURCE_I2C2_MASTER); // clear interrupt flag first because next event may complete before ISR returns
    const int index = currentMessage->index++;
    switch (currentMessage->event[index]) {
        case I2CMessageEventStart:
            I2C2CONbits.SEN = 1;
            break;
        case I2CMessageEventRepeatedStart:
            I2C2CONbits.RSEN = 1;
            break;
        case I2CMessageEventStop:
            I2C2CONbits.PEN = 1;
            break;
        case I2CMessageEventSend:
            I2C2TRN = currentMessage->data[index];
            break;
        case I2CMessageEventReceive:
            I2C2CONbits.RCEN = 1;
            break;
        case I2CMessageEventAck:
            *currentMessage->destination[index] = I2C2RCV;
            I2C2CONbits.ACKDT = 0;
            I2C2CONbits.ACKEN = 1;
            break;
        case I2CMessageEventNack:
            *currentMessage->destination[index] = I2C2RCV;
            I2C2CONbits.ACKDT = 1;
            I2C2CONbits.ACKEN = 1;
            break;
        case I2CMessageEventEnd:
            EVIC_SourceDisable(INT_SOURCE_I2C2_MASTER);
            if (currentMessage->complete != NULL) {
                currentMessage->complete();
            }
            break;
    }
}

